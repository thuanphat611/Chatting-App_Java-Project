CREATE TABLE ACCOUNTS
(
    USERNAME NVARCHAR(30),
    PASSWORD NVARCHAR(30) NOT NULL

        CONSTRAINT PK_ACCOUNT
        PRIMARY KEY (USERNAME)
)
CREATE TABLE GROUPS
(
    GROUP_NAME NVARCHAR(100),
    OWNER NVARCHAR(30)

        CONSTRAINT PK_GROUPS
        PRIMARY KEY(GROUP_NAME, OWNER)
        CONSTRAINT FK_GROUPS
        FOREIGN KEY(OWNER)
        REFERENCES ACCOUNTS
)
CREATE TABLE GROUP_MEMBERS
(
    GROUP_NAME NVARCHAR(100),
    OWNER NVARCHAR(30),
    MEMBER_NAME NVARCHAR(30)

        CONSTRAINT PK_GROUP_MEMBERS
        PRIMARY KEY(GROUP_NAME, OWNER, MEMBER_NAME),
    CONSTRAINT FK_GM_G
        FOREIGN KEY (GROUP_NAME, OWNER)
            REFERENCES GROUPS,
    FOREIGN KEY(MEMBER_NAME)
        REFERENCES ACCOUNTS
)
CREATE TABLE CHAT_HISTORY
(
    SENDER NVARCHAR(30),
    RECEIVER NVARCHAR(30),
    CONTENT NVARCHAR(200) NOT NULL,
    ORDER_INDEX INT NOT NULL

        CONSTRAINT PK_CHAT_HISTORY
            PRIMARY KEY(ORDER_INDEX),
    CONSTRAINT FK_CH_A
        FOREIGN KEY(SENDER)
            REFERENCES ACCOUNTS,
    CONSTRAINT FK_CH_A2
        FOREIGN KEY(RECEIVER)
            REFERENCES ACCOUNTS,
)